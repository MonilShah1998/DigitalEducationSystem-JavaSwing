    public boolean validatePassword(String password) {
        Matcher matcher = pattern.matcher(password);
        return matcher.matches();
    }
    
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(txtPassword.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter Password");
            return;
        }
        
        if(!validatePassword(txtPassword.getText())){
            JOptionPane.showMessageDialog(this, "Password is too weak. Cover all the points");
            return;
        }
        
        if(utils.updatePassword(utils.hashPassword(txtPassword.getText()), user.getId())){
            txtPassword.setEnabled(false);
            btnUpdate.setEnabled(true);
            btnSave.setEnabled(false);
            JOptionPane.showMessageDialog(null, "User info updated successfully!");  
        }
        else{
            JOptionPane.showMessageDialog(null, "Error updating Password. Try again Later!");  
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed

