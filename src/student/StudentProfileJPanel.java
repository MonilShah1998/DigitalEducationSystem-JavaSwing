    public void PopulateTable(){
        DefaultTableModel tbl = (DefaultTableModel) tblCourses.getModel();
        tbl.setRowCount(0);
        ArrayList<UserToCourse> courses = utils.getCoursesByStudentId(user.getId());

        for(Course c : utils.getCoursesByCourseId(courses)){
            Object[] row = new Object[6];
            row[0] = c;
            row[1] = c.getName();
            row[2] = c.getProfName();
            row[3] = c.getTerm();
            row[4] = c.getCredits();
            row[5] = isCourseCompleted(courses, c.getID());
            
            tbl.addRow(row);
        }
    }
    
    private boolean isCourseCompleted(ArrayList<UserToCourse> userToCourses, int targetId){
        for(UserToCourse uc: userToCourses){
            if(uc.getCourseID()==targetId)
                return uc.isCompleted();
        }
        
        return false;
    }
    
