    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tablePersonList.getModel();
        model.setRowCount(0);
        for (Course course : utils.getCourse()) {
            Object[] row = new Object[6];
            row[0] = course;
            row[1] = course.getProfName();
            row[2] = course.getName();
            row[3] = course.getCredits();
            row[4] = course.getRanking();
            row[5] = course.getProfId();
            model.addRow(row);
        }
    }
