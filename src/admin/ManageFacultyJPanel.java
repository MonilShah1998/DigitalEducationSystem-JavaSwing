    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        //Get selected Course
        int selectedRowIndex = tablePersonList.getSelectedRow();

        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please Select a row to view.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) tablePersonList.getModel();
        Object course = (Course) model.getValueAt(selectedRowIndex, 0);

        if(course instanceof Course c){
            ViewProfAdmin panel = new ViewProfAdmin(mainPanel, c, utils);
            mainPanel.add("ViewProfAdmin",panel);
            CardLayout layout = (CardLayout) mainPanel.getLayout();
            layout.next(mainPanel);
        }
        else{
            JOptionPane.showMessageDialog(this, "Invalid data in the selected row.");
        }
    }//GEN-LAST:event_btnViewActionPerformed

