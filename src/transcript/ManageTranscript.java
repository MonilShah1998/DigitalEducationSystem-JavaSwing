    public void PopulateTable(){
        DefaultTableModel tbl = (DefaultTableModel) tblCourses.getModel();
        tbl.setRowCount(0);
        for(UserToCourse c : utils.getCoursesByStudentId(transcript.getStudentId())){
            Object[] row = new Object[5];
            row[0] = c;
            row[1] = c.getAttendance();
            row[2] = c.getScore();
            row[3] = c.isCompleted();
            row[4] = c.getRemarks();
            
            tbl.addRow(row);
        }
    }
