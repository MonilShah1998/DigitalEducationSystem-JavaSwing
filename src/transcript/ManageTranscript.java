    private void backAction(){
        mainPanel.remove(this);
        CardLayout layout = (CardLayout) mainPanel.getLayout();
        layout.previous(mainPanel);
        
        Component component = mainPanel.getComponent(mainPanel.getComponentCount() - 1);
        if(component instanceof AuthorityDashboard authorityDashboard) {
            authorityDashboard.PopulateTable();
        }
    }
    
    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        if(utils.updateTranscriptStatus(transcript.getStudentId(), "Approved")){
            JOptionPane.showMessageDialog(this, "Transcript has been approved");
            backAction();
            
        }else{
            JOptionPane.showMessageDialog(this, "Error while approving");
        }
        
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnRejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejectActionPerformed
        if(utils.updateTranscriptStatus(transcript.getStudentId(), "Rejected")){
            JOptionPane.showMessageDialog(this, "Transcript has been Rejected");
            backAction();
        }else{
            JOptionPane.showMessageDialog(this, "Error while Rejecting");
        }
    }//GEN-LAST:event_btnRejectActionPerformed


